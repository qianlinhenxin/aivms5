package com.qlhx.service.wechat.realize.model;

public class VistorSmsInfo {
	
	private String vistorName;
	private String vistorTime;
	private String employeeName;
	private String employeeDep;
	private String qrCode;
	public String getVistorName() {
		return vistorName;
	}
	public void setVistorName(String vistorName) {
		this.vistorName = vistorName;
	}
	public String getVistorTime() {
		return vistorTime;
	}
	public void setVistorTime(String vistorTime) {
		this.vistorTime = vistorTime;
	}
	public String getEmployeeName() {
		return employeeName;
	}
	public void setEmployeeName(String employeeName) {
		this.employeeName = employeeName;
	}
	public String getQrCode() {
		return qrCode;
	}
	public void setQrCode(String qrCode) {
		this.qrCode = qrCode;
	}
	public String getEmployeeDep() {
		return employeeDep;
	}
	public void setEmployeeDep(String employeeDep) {
		this.employeeDep = employeeDep;
	}
	

}
